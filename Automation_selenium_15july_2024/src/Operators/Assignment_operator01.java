package Operators;

public class Assignment_operator01 {

	public static void main(String[] args) {
		
		int a=10; // Local Variable defining
		
		System.out.println(a); // 10
		System.out.println(a += 3); // 13 increase as per mention value
		
		System.out.println(a); //  13
		System.out.println(a -= 10); // 3 decrease as per mention value
		
		System.out.println(a); // 3 
		System.out.println(a *= 2); // 6 multiply as per mention value
		
		System.out.println(a); // 6
		System.out.println(a /= 2); // 3 divide as per mention value
	}

}
